package chapters.chapter_05.CheckPoints_05;

public class CheckPoint_05_08 {
public static void main(String[] args) {
	int sum = 0;
	for (int i = 0; i < 10; ++i) {
		 sum += i;
		 System.out.println(sum);
	}
	sum = 0;
	System.out.println("--------------------------------------------------------------------------");
	
	 for (int i = 0; i < 10; i++) {
		sum += i;
		System.out.println(sum);
	}
}
}
